<?php
    $n = 11;

    for ($i=1; $i < 10; $i++) { 
        for ($j=1; $j < 10; $j++) { 
            for ($k=1; $k < 10; $k++) { 
                for ($l=1; $l < 10; $l++) { 
                    if($i + $j + $k + $l === $n){
                        echo $i . ' + ' . $j . ' + ' . $k . ' + ' . $l . ' = ' . $n;
                        echo '<br>';
                    }
                }
            }
        }
    }
?>